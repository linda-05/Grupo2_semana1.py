# declarando numeros
numero_1 = 8
numero_2 = 2

# suma
print("La suma de", numero_1, "y", numero_2, "es:", numero_1 + numero_2)

# diferencia
print("La diferencia de", numero_1, "y", numero_2, "es:", numero_1 - numero_2)

# prducto
print("El producto de", numero_1, "y", numero_2, "es:", numero_1 * numero_2)

#division
print("La division de", numero_1, "y", numero_2, "es:", numero_1 // numero_2)

# numero mayor
if numero_1 > numero_2:
    print("El numero mayor es:", numero_1)
elif numero_2 > numero_1: 
    print("El numero mayor es:", numero_2)